    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //  $user = Auth::user();
        //  if($user->role !== 'user'){
        //  return redirect('/dashboard');
        // }
        $user = User::find($id);
        return view('users.edit', [
